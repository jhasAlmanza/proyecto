<h2>Hola, ¿cómo te podemos ayudar?</h2>
<h4>¿No encuentras tus entradas?</h4>

<div>Escribe y selecciona el nombre del evento que quieres recuperar las entradas, rellena el campo con tu email y se te reenviarán automáticamente las entradas.</div>
<h4>No puedo descargar mis entradas</h4>

<div>Si al intentar descargar tus entradas, ves el mensaje 'No se han encontrado entradas' es probable que estés intentando descargar tus entradas desde un email que no utilizaste para adquirirlas o no es el que tienes abierto en Drados en este momento. Para conseguir descargarlas, puedes seguir los siguientes pasos:
	<ul>
    <li>Cierra la sesión que tengas abierta en Drados</li>
    <li>Vuelve al email donde tienes tus entradas y pincha en "Imprimir entradas"</li>
    <li>Introduce el email donde te llegaron las entradas y tu contraseña, si no la recuerdas te enviamos otra en "Recuperar contraseña"</li>
	</ul>
</div>
<h4>¿Cómo funciona Drados?</h4>

<div>Tus conciertos y festivales recomendados según tus géneros y artistas favoritos. Así de sencillo. Dinos cuál es la música que más te gusta y te avisaremos cuando hayan conciertos de tus artistas preferidos. Además, podrás saber qué amigos y conocidos van a ir o quieren ir y podrás crear un plan con ellos gracias a nuestro chat multiplataforma.</div>
<h4>Quiero crear un plan con mis amigos para ir a un festival. ¿Cómo empiezo?</h4>

<div>Añade a tus amigos, sigue el festival que te interese y abre un chat privado para comentar todos los detalles. Diviértete!</div>
<h4>¿Mi información de Drados es pública?</h4>

<div>No. Drados es una red social privada y segura. No indexamos información en ningún buscador.</div>
<h4>Quiero añadir todos los grupos que escucho normalmente. ¿Cómo lo hago?</h4>

<div>La forma más rápida es hacerlo automáticamente, importando tus listas más escuchadas en Spotify, Deezer, Last.fm o tu librería de iTunes. Para hacerlo, dirígete a la pestaña de música, haz click en el icono del servicio que utilizas y sigue las instrucciones.</div>
<h4>No encuentro un artista o un concierto, ¿Cómo lo puedo añadir?</h4>

<div>¡Lo sentimos! En nuestra página de contacto podrás escribirnos para añadir los artistas que no encuentres.</div>
<h4>¿Cómo puedo encontrar gente con mis mismos gustos musicales?</h4>

<div>En Drados te diremos quién quiere ir a cada concierto. También quién es fan de cada atista. Pero como si no fuera suficiente, en la sección de Música [Añadir link a: http://www.drados.es/] te recomendaremos usuarios afines a tus gustos musicales. ¡Crea tu comunidad!</div>
<h4>¿Pueden eliminar mi perfil en Drados?</h4>
<div>
Sí. En el caso de que incumplas nuestros Términos de Uso nos veremos obligados a cerrar tu cuenta de Drados.</div>
